#include <cstdio>
#include <vector>

int main()
{
	std::vector<int> expected = {0x73, 0x4c, 0x73, 0x74, 0x76, 0x6a, 0x5f, 0x64, 0x76, 0x6a, 0x26, 0x24, 0x20, 0x4e, 0x28, 0x6a, 0x2, 0x49, 0x2c, 0x31, 0x31, 0x2c, 0x78, 0x20};
	int now1 = 99;
	int now2 = 1;
	for (int i : expected)
	{
		int res = i ^ now1;
		int j = 1;
		while (res <= 0xC3)
		{
			res = (i + 128 * j) ^ now1;
			++j;
		}
		res -= 0xC3;
		res ^= now2;
		putchar(res);
		now1 -= 2;
		now2 += 2;
	}
	return 0;
}
